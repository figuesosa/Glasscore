    private void cargarEmpleados() {
        try{

        cmbEmpleado.removeAllItems();

        Connection con =
                ConexionDB.getConexion();

        String sql =
                "SELECT id_empleado,"
                + "nombre,"
                + "apellido "
                + "FROM empleados "
                + "WHERE estado='Activo'";

        PreparedStatement ps =
                con.prepareStatement(sql);

        ResultSet rs =
                ps.executeQuery();

        while(rs.next()){

            cmbEmpleado.addItem(

                rs.getInt("id_empleado")
                + " - "
                + rs.getString("nombre")
                + " "
                + rs.getString("apellido")

            );
        }

    }
    catch(SQLException e){

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());
    }
}

    private void cargarTabla() {
        try{

        DefaultTableModel modelo =
                (DefaultTableModel)
                tblAsignaciones.getModel();

        modelo.setRowCount(0);

        Connection con =
                ConexionDB.getConexion();

        String sql =
                "SELECT a.id_asignacion,"
                + "h.nombre_herramienta,"
                + "e.nombre + ' ' + e.apellido AS empleado,"
                + "a.fecha_salida,"
                + "a.fecha_devolucion_real,"
                + "a.observaciones "
                + "FROM asignacion_herramientas a "
                + "INNER JOIN herramientas h "
                + "ON a.id_herramienta=h.id_herramienta "
                + "INNER JOIN empleados e "
                + "ON a.id_empleado=e.id_empleado";

        PreparedStatement ps =
                con.prepareStatement(sql);

        ResultSet rs =
                ps.executeQuery();

        while(rs.next()){

            modelo.addRow(new Object[]{

                rs.getInt("id_asignacion"),
                rs.getString("nombre_herramienta"),
                rs.getString("empleado"),
                rs.getString("fecha_salida"),
                rs.getString("fecha_devolucion_real"),
                rs.getString("observaciones")

            });

        }

    }
    catch(SQLException e){

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());
    }
}

    private void cargarHerramientas() {
         try{

        cmbHerramienta.removeAllItems();

        Connection con =
                ConexionDB.getConexion();

        String sql =
                "SELECT id_herramienta,"
                + "nombre_herramienta "
                + "FROM herramientas "
                + "WHERE disponibilidad='Disponible'";

        PreparedStatement ps =
                con.prepareStatement(sql);

        ResultSet rs =
                ps.executeQuery();

        while(rs.next()){

            cmbHerramienta.addItem(

                rs.getInt("id_herramienta")
                + " - "
                + rs.getString("nombre_herramienta")

            );
        }

    }
    catch(SQLException e){

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());
    }
}
