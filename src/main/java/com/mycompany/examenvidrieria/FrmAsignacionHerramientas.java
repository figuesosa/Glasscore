    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
     try{

        int fila =
                tblAsignaciones.getSelectedRow();

        if(fila == -1){

            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione una asignacion");

            return;
        }

        Object fechaDevolucion = tblAsignaciones.getValueAt(fila, 4);
        if (fechaDevolucion != null && !fechaDevolucion.toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Esta asignacion ya fue devuelta");
            return;
        }

        int idAsignacion =
                Integer.parseInt(
                        tblAsignaciones
                        .getValueAt(fila,0)
                        .toString());

        Connection con =
                ConexionDB.getConexion();

        String sqlBuscar =
                "SELECT id_herramienta "
                + "FROM asignacion_herramientas "
                + "WHERE id_asignacion=?";

        PreparedStatement psBuscar =
                con.prepareStatement(sqlBuscar);

        psBuscar.setInt(1,idAsignacion);

        ResultSet rs =
                psBuscar.executeQuery();

        int idHerramienta = 0;

        if(rs.next()){

            idHerramienta =
                    rs.getInt("id_herramienta");
        }

        String sqlActualizar =
                "UPDATE asignacion_herramientas "
                + "SET fecha_devolucion_real=GETDATE() "
                + "WHERE id_asignacion=?";

        PreparedStatement psActualizar =
                con.prepareStatement(sqlActualizar);

        psActualizar.setInt(1,idAsignacion);

        psActualizar.executeUpdate();

        String sqlHerramienta =
                "UPDATE herramientas "
                + "SET disponibilidad='Disponible' "
                + "WHERE id_herramienta=?";

        PreparedStatement psHerramienta =
                con.prepareStatement(sqlHerramienta);

        psHerramienta.setInt(
                1,
                idHerramienta);

        psHerramienta.executeUpdate();

        JOptionPane.showMessageDialog(
                this,
                "Herramienta devuelta correctamente");

        cargarTabla();
        cargarHerramientas();

    }
    catch(Exception e){

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());
    }

    }//GEN-LAST:event_btnDevolverActionPerformed

