    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
    try{
        if (cmbHerramienta.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "No hay herramientas disponibles");
            return;
        }
        if (cmbEmpleado.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Seleccione un empleado");
            return;
        }

        String datoHerramienta =
                cmbHerramienta
                .getSelectedItem()
                .toString();

        int idHerramienta =
                Integer.parseInt(
                        datoHerramienta
                        .split("-")[0]
                        .trim());

        String datoEmpleado =
                cmbEmpleado
                .getSelectedItem()
                .toString();

        int idEmpleado =
                Integer.parseInt(
                        datoEmpleado
                        .split("-")[0]
                        .trim());

        Connection con =
                ConexionDB.getConexion();

        String sql =
                "INSERT INTO asignacion_herramientas "
                + "(id_herramienta,"
                + "id_empleado,"
                + "fecha_salida,"
                + "observaciones) "
                + "VALUES "
                + "(?,?,GETDATE(),?)";

        PreparedStatement ps =
                con.prepareStatement(sql);

        ps.setInt(1,idHerramienta);
        ps.setInt(2,idEmpleado);
        ps.setString(
                3,
                txtObservaciones.getText());

        ps.executeUpdate();

        String sql2 =
                "UPDATE herramientas "
                + "SET disponibilidad='Asignada' "
                + "WHERE id_herramienta=?";

        PreparedStatement ps2 =
                con.prepareStatement(sql2);

        ps2.setInt(1,idHerramienta);

        ps2.executeUpdate();

        JOptionPane.showMessageDialog(
                this,
                "Herramienta asignada");

        cargarTabla();
        cargarHerramientas();

    }
    catch(HeadlessException | NumberFormatException | SQLException e){

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());
    }
    }//GEN-LAST:event_btnAsignarActionPerformed

